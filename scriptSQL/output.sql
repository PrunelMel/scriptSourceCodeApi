PRAGMA foreign_key = ON;
CREATE TABLE DEP(
    id_dep INTEGER PRIMARY KEY AUTOINCREMENT,
    CDP INT,
    DP VARCHAR(100)
);

CREATE TABLE CM(
    id_com INTEGER PRIMARY KEY AUTOINCREMENT,
    CCM INT,
    CM VARCHAR(100),
    id_dep INTEGER,
    FOREIGN KEY (id_dep) REFERENCES DEP (CDP)
);

CREATE TABLE AR(
    id_ar INTEGER PRIMARY KEY AUTOINCREMENT,
    CAR INT,
    AR VARCHAR(100),
    id_com INTEGER,
    FOREIGN KEY (id_cm) REFERENCES CM (CCM)
);


CREATE TABLE VQ(
    id_vq INTEGER PRIMARY KEY AUTOINCREMENT,
     VQ VARCHAR(100),
    id_arr INTEGER,
    FOREIGN KEY (id_ar) REFERENCES AR (CAR)
);